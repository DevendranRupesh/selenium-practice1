package org.controlstmt;

public class LoopingStatement {
	
	
	//While loop
	//Initialization int i= value
	//Condition While(i<=value){
	//Statement
	//incremental or deceremental
	//}
	
	public static void main(String[] args) {
		
         int i=1; //initialization
		
		while(i<=10) { //condition
			
			System.out.println(i); //Statement
			
			i++; //incremental or decremental
		}
		
		
		//Dowhile
		//initialization int i= value
		//Do{ Statement
		
		//incremental or deceremental
		//} while(i<=value);
		
		
		int j=1; //Initialization
		
		do {
			System.out.println(j);
			j++;
			
		}
		while(j<=10);
		
     //for loop
	
	//for each   Nested for loop
	
	//for(initialization, condition, incremental );
	
	for(int k=1;k<=10;k++) {
		
		System.out.println(k);
		
		
		
	}
	
}


}
		
			
		
		
		
		
		
		
		
		
		
	


