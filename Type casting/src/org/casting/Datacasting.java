package org.casting;

public class Datacasting {

	public static void main(String[] args) {
		
		int a=50;
		double value =a;// widening changing datatypes from big data types to small datatypes
		
		System.out.println(value);
		
		short b=45;
		float answer=b;// widening
		
		System.out.println(answer);
		
	}
	
}

	
	
	
	
	
	


